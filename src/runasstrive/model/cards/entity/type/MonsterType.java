package runasstrive.model.cards.entity.type;

/**
 * This enum represents the various monster element types,
 * including the 'none' type to represent non element monsters.
 *
 * @author ugget
 * @version 1.0
 */
public enum MonsterType {
    /**
     * the Ice element type
     */
    ICE,

    /**
     * the Fire element type
     */
    FIRE,

    /**
     * the Lightning element type
     */
    LIGHTNING,

    /**
     * the Water element type
     */
    WATER,

    /**
     * the None element type
     */
    NONE
}
